package Javaextra;

public class Casting_nd_constants {
    public static void main(String[] args) {
         int p = 100;
         float p1 = (float)p;
        System.out.println(p1);
         float fp = p + 199.99f;
        System.out.println(fp);
        System.out.println((int)19.9);

        // constant
        final float PI = 3.14F;
        System.out.println(PI);
        //
    }
}
